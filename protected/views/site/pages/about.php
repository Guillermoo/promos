<h1>Qué es ProEmoción</h1>
<div class="clearfix">&nbsp;</div>
<div class="textopro">
<p>ProEmoción es una página web creada para difundir las promociones y donde se puede obtener también un espacio fijo para desarrollar una tienda on-line.</p>
<div class="clearfix">&nbsp;</div>
<p>
Esta plataforma va dirigida a todos aquellos comercios de venta al público, ya sea de servicios o bienes. No es necesario que tengan presencia on-line.</p>
<div class="clearfix">&nbsp;</div>

<p>
El fin de difundir esas promociones es atraer a clientes nuevos a quienes puedan interesar los productos anunciados, además de que los ya clientes, puedan disfrutar también esa promoción. Existe un sistema de puntuaciones donde se da una valoración objetiva y real de las promociones por parte del usuario final.</p>
<div class="clearfix">&nbsp;</div>
<p>
 Las promociones duran un tiempo determinado. El cliente final, puede comprarlas a través de internet o en el propio establecimiento, mediante un cupón que se consigue en ProEmoción, dentro de los límites promocionales establecidos por el mismo comercio.</p>
