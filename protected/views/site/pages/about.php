<h1>Qué es Proemoción</h1>
<div class="clearfix">&nbsp;</div>
<div class="textopro">
<p>Proemoción es una página web creada para difundir las promociones y donde se puede obtener también un espacio fijo para desarrollar una tienda on-line.</p>
<div class="clearfix">&nbsp;</div>
<p>
Esta plataforma va dirigida a todos aquellos comercios de venta al público, ya sea de servicios o bienes. No es necesario que tengan presencia on-line.</p>
<div class="clearfix">&nbsp;</div>

<p>
El fin de difundir esas promociones es atraer a clientes nuevos a quienes puedan interesar los productos anunciados, además de que los ya clientes, puedan disfrutar también esa promoción. Existe un sistema de puntuaciones donde se da una valoración objetiva y real de las promociones por parte del usuario final.</p>
<div class="clearfix">&nbsp;</div>
<p>
 Las promociones duran un tiempo determinado. El cliente final, puede comprarlas a través de internet o en el propio establecimiento, mediante un cupón que se consigue en Proemoción, dentro de los límites promocionales establecidos por el mismo comercio.</p>
<div class="clearfix">&nbsp;</div>
<center><img src="<?php echo Yii::app()->request->baseUrl.'/img/tu_web.jpg' ?>"  alt="tu web de promociones" class="rounded"></center>
<div class="clearfix">&nbsp;</div>
 <p>
Nuestros clientes anunciantes disponen de una interfaz muy sencilla para desarrollar su anuncio de promoción, de esta forma el cliente elige en todo momento sus propias promociones cómo y cuándo quiere, y reciben de forma íntegra e inmediata el importe de cada venta realizada on-line. 
<div class="clearfix">&nbsp;</div>
<p>
Proemoción únicamente hace de difusor para que las promociones lleguen a los suscriptores de nuestra página. También estamos presentes en las redes sociales Facebook y Twitter.
<div class="clearfix">&nbsp;</div>
<p>
Disponemos de un servicio personalizado de asesoramiento para el desarrollo de promociones y creación de los anuncios de las mismas.</p>
<div class="clearfix">&nbsp;</div>

<p>Proemoción te permite registrarte como empresa y acceder al panel de usuario, ver todas sus opciones y crear tus promociones sin coste alguno para que compruebes lo fácil que resulta crear tu propio anuncio.
Proemoción le solicitará el pago del bono que elijas cuando decidas publicar tus promociones, y no antes.</p>
